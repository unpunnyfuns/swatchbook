/**
 * Generates the animated header logo SVGs under `apps/docs/src/assets/`.
 *
 * The mark is a fixed yin-yang badge; behind it a conic sweep of the
 * `gradient.cool` token (green at the top, blue at the bottom, light-blue
 * on the sides) spins slowly. SVG has no native conic gradient and a
 * `conic-gradient()` in `<foreignObject>` doesn't render inside an `<img>`
 * (which is how Starlight mounts the logo), so the conic is approximated
 * with `SEGS` angular wedges and that group is rotated via CSS. The spin
 * is frozen under `prefers-reduced-motion`.
 *
 * `light-logo.svg` is the design as-is; `dark-logo.svg` is the same design
 * rotated 180deg (the mark inverts and green lands at the bottom), wired to
 * Starlight's `logo.light` / `logo.dark` because an `<img>` SVG can't read
 * the page's theme vars. Both files are generated: edit this script, not
 * them. Run with `pnpm --filter @unpunnyfuns/swatchbook-docs gen:logo`.
 */
import { writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const assets = resolve(dirname(fileURLToPath(import.meta.url)), '../src/assets');

// gradient.cool token, keyed by angle (0deg = up, clockwise). Green (the
// light end) sits at the top; the dark variant rotates the whole thing 180deg.
const KEYS = [
  [0, [0x86, 0xef, 0xac]],
  [90, [0x60, 0xa5, 0xfa]],
  [180, [0x1d, 0x4e, 0xd8]],
  [270, [0x60, 0xa5, 0xfa]],
  [360, [0x86, 0xef, 0xac]],
];

const hex = ([r, g, b]) =>
  '#' + [r, g, b].map((n) => Math.round(n).toString(16).padStart(2, '0')).join('');

function colorAt(angle) {
  const a = ((angle % 360) + 360) % 360;
  for (let i = 0; i < KEYS.length - 1; i++) {
    const [a0, c0] = KEYS[i];
    const [a1, c1] = KEYS[i + 1];
    if (a >= a0 && a <= a1) {
      const t = (a - a0) / (a1 - a0);
      return hex(c0.map((v, k) => v + (c1[k] - v) * t));
    }
  }
  return hex(KEYS[0][1]);
}

const CX = 12;
const CY = 12;
const R = 16;
const SEGS = 96;
const STEP = 360 / SEGS;

function point(deg) {
  const rad = ((deg - 90) * Math.PI) / 180;
  return [CX + R * Math.cos(rad), CY + R * Math.sin(rad)];
}

const f = (n) => n.toFixed(3).replace(/\.?0+$/, '');

function conicWedges() {
  const wedges = [];
  for (let i = 0; i < SEGS; i++) {
    // 0.3deg of overlap on each side keeps anti-aliasing from leaving seams.
    const [x0, y0] = point(i * STEP - 0.3);
    const [x1, y1] = point((i + 1) * STEP + 0.3);
    const color = colorAt((i + 0.5) * STEP);
    wedges.push(`<path fill="${color}" d="M${CX} ${CY} L${f(x0)} ${f(y0)} A${R} ${R} 0 0 1 ${f(x1)} ${f(y1)} Z"/>`);
  }
  return wedges.join('\n      ');
}

const HEX =
  'M9.166.33a2.25 2.25 0 00-2.332 0l-5.25 3.182A2.25 2.25 0 00.5 5.436v5.128a2.25 2.25 0 001.084 1.924l5.25 3.182a2.25 2.25 0 002.332 0l5.25-3.182a2.25 2.25 0 001.084-1.924V5.436a2.25 2.25 0 00-1.084-1.924L9.166.33z';
const DOT =
  'M14 16C14 17.1046 13.1046 18 12 18C10.8954 18 10 17.1046 10 16C10 14.8954 10.8954 14 12 14C13.1046 14 14 14.8954 14 16Z';
const YIN =
  'M22 12C22 17.5228 17.5228 22 12 22C6.47715 22 2 17.5228 2 12C2 6.47715 6.47715 2 12 2C17.5228 2 22 6.47715 22 12ZM12 12C9.79086 12 8 10.2091 8 8C8 5.79086 9.79086 4 12 4C7.58172 4 4 7.58172 4 12C4 16.4183 7.58172 20 12 20C14.2091 20 16 18.2091 16 16C16 13.7909 14.2091 12 12 12ZM14 8C14 9.10457 13.1046 10 12 10C10.8954 10 10 9.10457 10 8C10 6.89543 10.8954 6 12 6C13.1046 6 14 6.89543 14 8Z';

const defs = `  <defs>
    <clipPath id="badge"><path transform="translate(-2.4 -2.4) scale(1.8)" d="${HEX}"/></clipPath>
    <style>
      .sb-spin { animation: sb-spin 24s linear infinite; transform-origin: 12px 12px; transform-box: view-box; }
      @keyframes sb-spin { to { transform: rotate(360deg); } }
      @media (prefers-reduced-motion: reduce) { .sb-spin { animation: none; } }
    </style>
  </defs>`;

const content = `  <!-- Badge: spinning conic (gradient.cool token, green/blue poles) -->
  <g clip-path="url(#badge)">
    <g class="sb-spin">
      ${conicWedges()}
    </g>
  </g>
  <!-- Token mark; 80% opacity lets the conic tint it -->
  <g fill="#1e293b" opacity="0.8">
    <path d="${DOT}"/>
    <path fill-rule="evenodd" clip-rule="evenodd" d="${YIN}"/>
  </g>`;

const open =
  '<svg xmlns="http://www.w3.org/2000/svg" viewBox="-2.4 -2.4 28.8 28.8" width="256" height="256" role="img" aria-label="swatchbook">';
const note = '  <!-- Generated by scripts/gen-logo.mjs. Edit that script, not this file. -->';

const light = `${open}\n${note}\n${defs}\n${content}\n</svg>\n`;
const dark = `${open}\n${note}\n${defs}\n  <g transform="rotate(180 12 12)">\n  ${content.replace(/\n/g, '\n  ')}\n  </g>\n</svg>\n`;

writeFileSync(resolve(assets, 'light-logo.svg'), light);
writeFileSync(resolve(assets, 'dark-logo.svg'), dark);
console.log(`[docs:gen-logo] wrote light-logo.svg + dark-logo.svg (${SEGS} conic segments; dark = light rotated 180deg)`);
